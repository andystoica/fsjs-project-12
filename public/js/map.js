function initMap() {

    var map = new google.maps.Map($('#tracker')[0], {
        zoom: 2,
        maxZoom: 10,
        center: {
            lat: 20,
            lng: 0
        },
        styles: colourScheme,
        mapTypeControl: false,
        rotateControl: false,
        streetViewControl: false
    });
    var markers = [];
    var infoWindows = [];

  
    // Retreive ISS location and ground weather information and create a marker on the map
    function locateIss() {
        $('#locate').addClass('disabled');
        $.get('/api/location', function (res) {
            
            // Create a new marker
            var marker = new google.maps.Marker({
                position: { lat: res.lat, lng: res.lng },
                icon: '/img/satellite-icon.svg',
                map: map,
                title: res.date + ', ' + res.time,
            });

            // Content for the information window when marker is clicked
            var contentString = '<div id="mapContent">' +
                '<div id="siteNotice"></div>' +
                '<div id="bodyContent">' +
                '<h3 class="firstHeading" id="firstHeading">ISS Location<h3>' +
                '<p>Tracked on: ' + res.date + ' at ' + res.time + '<br>' +
                'Local weather: ' + res.weather + '</p>' +
                '<p>Latitude: ' + res.lat + '<br>Longitude: ' + res.lng + '</p>' +
                '</div></div>';

            // Create the information window and attach to event
            var infoWindow = new google.maps.InfoWindow({
                content: contentString,
                maxWidth: 350
              });
            
            marker.addListener('click', function () {
                infoWindow.open(map, marker);
            });

            // Keep track of all markers
            markers.push(marker);
            $('#locate').removeClass('disabled');

            // Center and zoom map arround all known markers
            var bounds = new google.maps.LatLngBounds();
            markers.forEach(function (item) {
                bounds.extend(item.getPosition());
            });
            map.fitBounds(bounds, 50);
        });
    }

    // Bind #locate button to tracking function
    $('#locate').click(function(){
        locateIss();
    });
}

